L1 = [1,3,4,5,6,7,2,4]
#sort() update original list 
L1.sort()
print(L1)

#reverse() update original list 
L1.reverse()
print()

#append() add element at the end of the list, update original list
L1.append(19)
print(L1)

#insert(index, value) -> insert value at given index
L1.insert(8,3)
print(L1)

#pop(index) -> removes element at index
L1.pop(3) 
print(L1)

#remove(element) -> remove element from List
L1.remove(19)
print(L1)